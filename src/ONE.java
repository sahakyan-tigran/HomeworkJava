public class ONE {
    public static void main(String[] args) {
        byte x = 5;
        System.out.println(x);
        short y = 15;
        System.out.println(y);
        int z = 115;
        System.out.println(z);
        long w = 1115L;
        System.out.println(w);
        float c = 14.3F;
        System.out.println(c);
        double d = 456.2;
        System.out.println(d);
        char f = '!';
        System.out.println(f);
        boolean flag = true;
        System.out.println(flag);
    }
}
